import pandas as pd

# dosyalari yüklüyoruz ve isimlerini eşleştiriyoruz
files = ['Aligned.csv', 'Flocking.csv', 'Grouped.csv']


df_list = []
for f in files:
    try:
        # ilk boş satırı atlıyoruz
        temp_df = pd.read_csv(f, skiprows=[0], encoding='latin1')
        df_list.append(temp_df)
        print(f"{f} başarıyla okundu. Boyut: {temp_df.shape}")
    except Exception as e:
        print(f"{f} okunurken hata oluştu: {e}")

# tabloda birleştirme
df = pd.concat(df_list, ignore_index=True)

# boşlukları temizleme
df.columns = df.columns.str.strip()

