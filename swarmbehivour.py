import pandas as pd
from sklearn.preprocessing import StandardScaler
from sklearn.decomposition import PCA
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import classification_report, confusion_matrix, accuracy_score
import seaborn as sns
import matplotlib.pyplot as plt

#veriyi hazırlama
target_column = df.columns[-1]
X = df.iloc[:, :-1]
y = df.iloc[:, -1]


X = X.apply(pd.to_numeric, errors='coerce').fillna(0)

# PCA
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

pca = PCA(n_components=2)
X_pca = pca.fit_transform(X_scaled)

# Model eğitimi knn kullanımı
X_train, X_test, y_train, y_test = train_test_split(X_pca, y, test_size=0.2, random_state=42)

from sklearn.neighbors import KNeighborsClassifier
model = KNeighborsClassifier(n_neighbors=5)
model.fit(X_train, y_train)

#  görselleştirme
y_pred = model.predict(X_test)

print(" PROJE SONUÇLARI ")
print(f"Başarı Oranı (Accuracy): %{accuracy_score(y_test, y_pred)*100:.5f}")
print("\nSınıflandırma Raporu:")
print(classification_report(y_test, y_pred, digits=5))

# hata matrisi
plt.figure(figsize=(6,4))
sns.heatmap(confusion_matrix(y_test, y_pred), annot=True, fmt='d', cmap='Blues')
plt.title('Hata Matrisi (Confusion Matrix) - KNN')
plt.xlabel('Tahmin Edilen')
plt.ylabel('Gerçek Değer')
plt.show()

# pca kümeleme grafiği
plt.figure(figsize=(10, 6))
plt.scatter(X_pca[:, 0], X_pca[:, 1], c=y, cmap='coolwarm', alpha=0.5)
plt.title('PCA ile Sürü Davranışı Analizi')
plt.colorbar(label='Sürü Durumu')
plt.show()

